    @staticmethod
    def _parse_declaration(text: str) -> Dict[str, str]:
        """Parse 'key:value|key:value' declaration string."""
        result = {}
        if not text:
            return result
        for part in text.split("|"):
            part = part.strip()
            if ":" in part:
                k, v = part.split(":", 1)
                result[k.strip()] = v.strip()
        return result


