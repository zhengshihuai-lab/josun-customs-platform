class DataLoader:
    """Reads Product Master and Purchase Order Excel files."""

    @staticmethod
    def load_product_master(filepath: str) -> Dict[str, Dict[str, Any]]:
        """Return dict keyed by product code."""
        logger.info("Loading product master: %s", filepath)
        if not os.path.isfile(filepath):
            raise FileNotFoundError(f"Product master not found: {filepath}")
        wb = openpyxl.load_workbook(filepath, data_only=True)
        ws = wb["Product Master"]
        headers = [cell.value for cell in ws[1]]
        products: Dict[str, Dict[str, Any]] = {}
        for row in ws.iter_rows(min_row=2, values_only=True):
            rec = dict(zip(headers, row))
            code = rec.get("產品內部編碼")
            if code:
                products[code] = rec
        wb.close()
        logger.info("Loaded %d products", len(products))
        return products

    @staticmethod
    def load_purchase_order(filepath: str) -> Tuple[Dict[str, str], List[Dict[str, Any]]]:
        """Return (header_info, detail_items).

        Supports multiple PO layouts:
          - Simple two-column key-value (A:B) in the header area
          - Multi-column layout where keys and values may appear in any column
        Keys are matched by prefix so that '訂單編號 Order No.:' still maps to '訂單編號'.
        """
        logger.info("Loading purchase order: %s", filepath)
        if not os.path.isfile(filepath):
            raise FileNotFoundError(f"Purchase order not found: {filepath}")
        wb = openpyxl.load_workbook(filepath, data_only=True)
        ws = wb["採購訂單"]

        # Canonical key mapping (prefix -> standard key name)
        KEY_MAP = {
            "訂單編號": "訂單編號",
            "訂單日期": "訂單日期",
            "客戶名稱": "客戶名稱",
            "客戶地址": "客戶地址",
            "目的港": "目的港",
            "貿易條款": "貿易條款",
            "付款方式": "付款方式",
            "合同編號": "合同編號",
            "預計出貨": "預計出貨日",
            "備註": "備註",
        }

        def _normalize_key(raw_key: str) -> Optional[str]:
            """Match a raw cell value to a canonical header key."""
            if not raw_key:
                return None
            raw_key = str(raw_key).strip()
            for prefix, canonical in KEY_MAP.items():
                if raw_key.startswith(prefix):
                    return canonical
            return None

        # Find detail header row first (look for '序號' in column A)
        detail_hdr_row = None
        for row in range(1, ws.max_row + 1):
            cell_val = ws.cell(row=row, column=1).value
            if cell_val is not None and str(cell_val).strip() == "序號":
                detail_hdr_row = row
                break
        if detail_hdr_row is None:
            raise ValueError("Cannot find detail header row (序號) in PO")

        # --- Parse header (all rows above detail header) ---
        header: Dict[str, str] = {}
        for row in range(1, detail_hdr_row):
            for col in range(1, ws.max_column + 1):
                raw = ws.cell(row=row, column=col).value
                canon = _normalize_key(raw)
                if canon is None:
                    continue
                # Value is the next non-empty cell to the right
                val = None
                for vc in range(col + 1, ws.max_column + 1):
                    candidate = ws.cell(row=row, column=vc).value
                    # skip if this candidate is itself a key
                    if candidate and _normalize_key(str(candidate)) is not None:
                        break
                    if candidate is not None and str(candidate).strip() != "":
                        val = candidate
                        break
                header[canon] = str(val).strip() if val else ""

        logger.info("PO header parsed: %s", {k: v for k, v in header.items() if v})

        # --- Parse detail items ---
        raw_headers = [ws.cell(row=detail_hdr_row, column=c).value for c in range(1, ws.max_column + 1)]
        # Normalize column names
        COL_ALIAS = {
            "產品編碼": "產品內部編碼",
            "型號": "型號規格",
        }
        detail_headers = []
        for h in raw_headers:
            h_str = str(h).strip() if h else ""
            detail_headers.append(COL_ALIAS.get(h_str, h_str))

        items: List[Dict[str, Any]] = []
        for row in range(detail_hdr_row + 1, ws.max_row + 1):
            vals = [ws.cell(row=row, column=c).value for c in range(1, ws.max_column + 1)]
            if all(v is None for v in vals):
                continue
            rec = dict(zip(detail_headers, vals))
            seq = rec.get("序號")
            if seq is not None and str(seq).strip().isdigit():
                # Cast numeric fields
                for nf in ("數量", "單價USD", "金額USD"):
                    if rec.get(nf) is not None:
                        try:
                            rec[nf] = float(rec[nf])
                        except (ValueError, TypeError):
                            pass
                items.append(rec)
        wb.close()
        logger.info("PO header: %s | Items: %d", header.get("訂單編號", "N/A"), len(items))
        return header, items


