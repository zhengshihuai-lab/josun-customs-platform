class CustomsDocumentTool:
    """Orchestrates the full document generation pipeline."""

    def __init__(self, product_path: str, po_path: str, output_dir: str):
        self.product_path = product_path
        self.po_path = po_path
        self.output_dir = output_dir

    def run(self) -> str:
        """Execute the full pipeline and return the output file path."""
        # Load data
        products = DataLoader.load_product_master(self.product_path)
        po_header, po_items = DataLoader.load_purchase_order(self.po_path)

        order_no = po_header.get("訂單編號", "UNKNOWN")
        date_str = datetime.now().strftime("%Y%m%d")
        # Sanitize order number for filesystem
        safe_order = "".join(c if c.isalnum() or c in "-_." else "_" for c in order_no)
        filename = f"報關單據_{safe_order}_{date_str}.xlsx"

        os.makedirs(self.output_dir, exist_ok=True)
        output_path = os.path.join(self.output_dir, filename)

        logger.info("Creating output workbook: %s", output_path)
        wb = openpyxl.Workbook()

        # Remove default sheet
        default_sheet = wb.active
        wb.remove(default_sheet)

        # Define generators with sheet names
        # Note: openpyxl forbids / \ * ? : [ ] in sheet titles
        generators = [
            ("商業發票 Commercial Invoice", CommercialInvoiceGenerator),
            ("裝箱單 Packing List", PackingListGenerator),
            ("提單資訊 BL Info", BillOfLadingGenerator),
            ("原產地證 CO Application", CertificateOfOriginGenerator),
            ("申報要素 Declaration Elements", DeclarationElementsGenerator),
            ("核對清單 Checklist", ChecklistGenerator),
        ]

        for sheet_name, gen_cls in generators:
            ws = wb.create_sheet(title=sheet_name)
            gen = gen_cls(ws, po_header, po_items, products)
            try:
                gen.generate()
                logger.info("  Sheet '%s' generated", sheet_name)
            except Exception as e:
                logger.error("  Sheet '%s' failed: %s", sheet_name, e)
                raise

        # Freeze panes and print setup for each sheet
        for ws in wb.worksheets:
            ws.sheet_properties.pageSetUpPr = openpyxl.worksheet.properties.PageSetupProperties(fitToPage=True)
            ws.page_setup.fitToWidth = 1
            ws.page_setup.fitToHeight = 0
            ws.page_setup.orientation = "portrait"
            ws.page_setup.paperSize = ws.PAPERSIZE_A4

        wb.save(output_path)
        wb.close()
        logger.info("=" * 60)
        logger.info("All documents generated successfully!")
        logger.info("Output: %s", output_path)
        logger.info("=" * 60)
        return output_path


