class CommercialInvoiceGenerator(DocumentGenerator):

    def __init__(self, ws, po_header, po_items, products):
        super().__init__(ws, po_header, po_items, products)
        self.max_col = 10

    def generate(self):
        logger.info("Generating Commercial Invoice ...")
        enriched = self._enrich_items()
        invoice_no = f"CI-{self.po.get('訂單編號', 'XXXX')}"
        po_date = self.po.get("訂單日期", datetime.now().strftime("%Y-%m-%d"))
        buyer = self.po.get("客戶名稱", "")
        buyer_addr = self.po.get("客戶地址", "")
        dest_port = self.po.get("目的港", "")
        trade_terms = self.po.get("貿易條款", "")
        payment = self.po.get("付款方式", "")
        contract_no = self.po.get("合同編號", "")

        # Title
        self._write_company_header(
            "商業發票", "COMMERCIAL INVOICE",
            f"Invoice No. / 發票號碼: {invoice_no}"
        )

        # Info block
        self._write_info_block([
            ("Invoice No. / 發票號碼", invoice_no),
            ("Date / 日期", po_date),
            ("Seller / 賣方", SELLER_NAME),
            ("Buyer / 買方", buyer),
            ("Address / 地址", SELLER_CN_ADDR),
            ("Buyer Address / 買方地址", buyer_addr),
            ("Contract No. / 合同編號", contract_no),
            ("Trade Terms / 貿易條款", trade_terms),
        ])
        self.current_row += 1

        # Shipping info
        self._write_info_block([
            ("From / 起運港", "Shanghai, China"),
            ("To / 目的港", dest_port),
            ("Shipping Marks / 嘜頭", f"N/M - {buyer[:20]}" if buyer else "N/M"),
            ("Payment / 付款方式", payment),
        ])
        self.current_row += 1

        # Detail table
        headers = [
            "Item No.\n項號", "Description of Goods (English)\n貨物描述（英文）",
            "Model\n型號", "HS Code\nHS編碼", "Quantity\n數量",
            "Unit\n單位", "Unit Price\n(USD)\n單價", "Amount\n(USD)\n金額",
            "Country of\nOrigin\n原產地", "Remarks\n備註"
        ]
        self._write_table_header(headers)

        total_qty = 0
        total_amount = 0.0
        for idx, item in enumerate(enriched):
            qty = item.get("數量", 0) or 0
            unit_price = item.get("單價USD", 0) or 0
            amount = qty * unit_price
            total_qty += qty
            total_amount += amount
            self._write_table_row(
                [
                    idx + 1,
                    item.get("英文品名", ""),
                    item.get("型號規格", ""),
                    item.get("HS編碼", ""),
                    qty,
                    item.get("單位", ""),
                    unit_price,
                    amount,
                    item.get("原產地", "China"),
                    item.get("備註", ""),
                ],
                number_fmt_cols=[7, 8],
                alt=(idx % 2 == 1),
            )

        # Totals
        self._write_total_row(
            ["", "TOTAL / 合計", "", "", total_qty, "", "", total_amount, "", ""],
            number_fmt_cols=[8],
        )
        self.current_row += 1

        # Summary block
        self._write_info_block([
            ("Total Quantity / 總數量", f"{total_qty}"),
            ("Total Amount / 總金額", f"USD {total_amount:,.2f}"),
            ("Trade Terms / 貿易條款", trade_terms),
            ("Payment Terms / 付款條件", payment),
        ])

        # Amount in words
        self.current_row += 1
        merge_and_write(self.ws, self.current_row, 1, self.max_col,
                        f"Say Total / 金額大寫: US DOLLARS {self._amount_to_words(total_amount)} ONLY",
                        FONT_INFO_KEY, ALIGN_LEFT, FILL_INFO, THIN_BORDER)

        self._write_signature_block()
        self._write_footer()
        self._set_column_widths({1: 8, 2: 32, 3: 16, 4: 16, 5: 10, 6: 8, 7: 14, 8: 16, 9: 12, 10: 18})

    @staticmethod
    def _amount_to_words(amount: float) -> str:
        """Simplified amount-to-words for USD."""
        whole = int(amount)
        cents = round((amount - whole) * 100)
        return f"{whole:,} AND CENTS {cents:02d}"


