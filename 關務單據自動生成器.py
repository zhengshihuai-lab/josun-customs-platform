def merge_and_write(ws, row: int, col_start: int, col_end: int,
                    value: Any, font: Font = FONT_INFO_VAL,
                    alignment: Alignment = ALIGN_LEFT,
                    fill: Optional[PatternFill] = None,
                    border: Optional[Border] = None):
    """Merge cells horizontally and write a value."""
    ws.merge_cells(start_row=row, start_column=col_start,
                   end_row=row, end_column=col_end)
    cell = ws.cell(row=row, column=col_start, value=value)
    cell.font = font
    cell.alignment = alignment
    if fill:
        cell.fill = fill
    if border:
        # Apply border to all cells in merge range
        for c in range(col_start, col_end + 1):
            ws.cell(row=row, column=c).border = border
    return cell


def set_row_border(ws, row: int, col_start: int, col_end: int,
                   border: Border = THIN_BORDER):
    for c in range(col_start, col_end + 1):
        ws.cell(row=row, column=c).border = border


