class DocumentGenerator:
    """Base class with common helpers for all document sheets."""

    def __init__(self, ws, po_header: Dict[str, str],
                 po_items: List[Dict[str, Any]],
                 products: Dict[str, Dict[str, Any]]):
        self.ws = ws
        self.po = po_header
        self.items = po_items
        self.products = products
        self.current_row = 1
        self.max_col = 10  # default, overridden by subclasses

    # -- Common builders ---------------------------------------------------

    def _write_company_header(self, title_cn: str, title_en: str, subtitle: str = ""):
        """Write company name + document title."""
        merge_and_write(self.ws, self.current_row, 1, self.max_col,
                        COMPANY_NAME, FONT_TITLE, ALIGN_CENTER)
        self.current_row += 1
        merge_and_write(self.ws, self.current_row, 1, self.max_col,
                        f"{title_cn} / {title_en}", FONT_SUBTITLE, ALIGN_CENTER)
        self.current_row += 1
        if subtitle:
            merge_and_write(self.ws, self.current_row, 1, self.max_col,
                            subtitle, FONT_SECTION, ALIGN_CENTER)
            self.current_row += 1
        self.current_row += 1  # blank row

    def _write_info_block(self, pairs: List[Tuple[str, str]], fill: PatternFill = FILL_INFO):
        """Write key-value info block spanning two columns each."""
        half = self.max_col // 2
        for i in range(0, len(pairs), 2):
            # Left pair
            k1, v1 = pairs[i]
            merge_and_write(self.ws, self.current_row, 1, 2, k1, FONT_INFO_KEY,
                            ALIGN_LEFT, fill, THIN_BORDER)
            merge_and_write(self.ws, self.current_row, 3, half, v1, FONT_INFO_VAL,
                            ALIGN_LEFT, fill, THIN_BORDER)
            # Right pair if available
            if i + 1 < len(pairs):
                k2, v2 = pairs[i + 1]
                merge_and_write(self.ws, self.current_row, half + 1, half + 2, k2,
                                FONT_INFO_KEY, ALIGN_LEFT, fill, THIN_BORDER)
                merge_and_write(self.ws, self.current_row, half + 3, self.max_col, v2,
                                FONT_INFO_VAL, ALIGN_LEFT, fill, THIN_BORDER)
            else:
                merge_and_write(self.ws, self.current_row, half + 1, self.max_col,
                                "", FONT_INFO_VAL, ALIGN_LEFT, fill, THIN_BORDER)
            self.current_row += 1

    def _write_table_header(self, headers: List[str]):
        for col_idx, h in enumerate(headers, 1):
            cell = self.ws.cell(row=self.current_row, column=col_idx, value=h)
            cell.font = FONT_TABLE_HDR
            cell.fill = FILL_HDR
            cell.alignment = ALIGN_CENTER
            cell.border = THIN_BORDER
        self.current_row += 1

    def _write_table_row(self, values: List[Any], bold_last: bool = False,
                         number_fmt_cols: Optional[List[int]] = None,
                         alt: bool = False):
        fill = FILL_ALT_ROW if alt else FILL_WHITE
        for col_idx, val in enumerate(values, 1):
            cell = self.ws.cell(row=self.current_row, column=col_idx, value=val)
            cell.font = FONT_TABLE_CELL_BOLD if (bold_last and col_idx == len(values)) else FONT_TABLE_CELL
            cell.border = THIN_BORDER
            cell.alignment = ALIGN_CENTER
            if fill:
                cell.fill = fill
            if number_fmt_cols and col_idx in number_fmt_cols:
                cell.number_format = '#,##0.00'
        self.current_row += 1

    def _write_total_row(self, values: List[Any], number_fmt_cols: Optional[List[int]] = None):
        for col_idx, val in enumerate(values, 1):
            cell = self.ws.cell(row=self.current_row, column=col_idx, value=val)
            cell.font = FONT_TABLE_CELL_BOLD
            cell.border = MEDIUM_BORDER
            cell.alignment = ALIGN_CENTER
            cell.fill = PatternFill(start_color=CLR_MED_GRAY, end_color=CLR_MED_GRAY, fill_type="solid")
            if number_fmt_cols and col_idx in number_fmt_cols:
                cell.number_format = '#,##0.00'
        self.current_row += 1

    def _write_footer(self):
        self.current_row += 1
        merge_and_write(self.ws, self.current_row, 1, self.max_col,
                        FOOTER_TEXT, FONT_FOOTER, ALIGN_CENTER)

    def _write_signature_block(self, labels: Optional[List[str]] = None):
        if labels is None:
            labels = ["Prepared by / 製表人", "Checked by / 核對人", "Approved by / 批准人"]
        self.current_row += 2
        col_span = self.max_col // len(labels)
        for i, label in enumerate(labels):
            start_col = i * col_span + 1
            end_col = start_col + col_span - 1
            merge_and_write(self.ws, self.current_row, start_col, end_col,
                            label, FONT_SIGN_LABEL, ALIGN_CENTER)
        self.current_row += 1
        for i in range(len(labels)):
            start_col = i * col_span + 1
            end_col = start_col + col_span - 1
            merge_and_write(self.ws, self.current_row, start_col, end_col,
                            "Signature / 簽名: ________________", FONT_SIGN_LABEL, ALIGN_LEFT)
        self.current_row += 1
        merge_and_write(self.ws, self.current_row, 1, self.max_col,
                        f"Date / 日期: ________________", FONT_SIGN_LABEL, ALIGN_RIGHT)

    def _set_column_widths(self, widths: Dict[int, float]):
        for col_idx, w in widths.items():
            self.ws.column_dimensions[get_column_letter(col_idx)].width = w

    def _enrich_items(self) -> List[Dict[str, Any]]:
        """Merge PO items with product master data."""
        enriched = []
        for item in self.items:
            code = item.get("產品內部編碼", "")
            prod = self.products.get(code, {})
            merged = {**prod, **{k: v for k, v in item.items() if v is not None}}
            enriched.append(merged)
        return enriched

    # -- Abstract-like method -----------------------------------------------
    def generate(self):
        raise NotImplementedError


